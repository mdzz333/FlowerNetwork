package com.flower.servlet;

import java.io.IOException;
import java.io.PrintWriter;
import java.util.List;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import com.flower.dao.FlowerDao;
import com.flower.entity.Flower;
import com.flower.entity.Type;

public class FindFlowerByTypeServlet extends HttpServlet {

	/**
	 * Constructor of the object.
	 */
	public FindFlowerByTypeServlet() {
		super();
	}

	/**
	 * Destruction of the servlet. <br>
	 */
	public void destroy() {
		super.destroy(); // Just puts "destroy" string in log
		// Put your code here
	}

	/**
	 * The doGet method of the servlet. <br>
	 * 
	 * This method is called when a form has its tag value method equals to get.
	 * 
	 * @param request
	 *            the request send by the client to the server
	 * @param response
	 *            the response send by the server to the client
	 * @throws ServletException
	 *             if an error occurred
	 * @throws IOException
	 *             if an error occurred
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		doPost(request, response);
	}

	/**
	 * The doPost method of the servlet. <br>
	 * 
	 * This method is called when a form has its tag value method equals to
	 * post.
	 * 
	 * @param request
	 *            the request send by the client to the server
	 * @param response
	 *            the response send by the server to the client
	 * @throws ServletException
	 *             if an error occurred
	 * @throws IOException
	 *             if an error occurred
	 */
	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		// 1.设置编码方式
		request.setCharacterEncoding("UTF-8");
		response.setCharacterEncoding("UTF-8");
		response.setContentType("type/html;charset=UTF-8");
		// 2.接收
		int tpId = new Integer(request.getParameter("tp_id"));
		// 3.封装参数
		Type type = new Type();
		type.setTpId(tpId);
		// 4.dao
		FlowerDao flowerDao = new FlowerDao();
		List<Flower> list = flowerDao.selectByFlName(type);
		request.setAttribute("all", list);

		// 获取鲜花 开始
		FlowerDao flowerDaoo = new FlowerDao();
		List<Flower> flower = flowerDaoo.selectAll();
		request.setAttribute("flowers", flower);
		// 获取鲜花 结束

		// 5.
		// response.sendRedirect("listAll.jsp");
		request.getRequestDispatcher("listAll.jsp").forward(request, response);

	}

	/**
	 * Initialization of the servlet. <br>
	 * 
	 * @throws ServletException
	 *             if an error occurs
	 */
	public void init() throws ServletException {
		// Put your code here
	}

}
